  // 获取当前语言 - 与父应用 react-i18next 同步
  function getCurrentLanguage() {
    // 优先读取父应用的i18next语言设置
    const parentLang = localStorage.getItem('i18nextLng');
    if (parentLang) {
      // 映射父应用语言代码到本模块支持的语言
      if (parentLang === 'zh-CN' || parentLang === 'zh-TW') return parentLang;
      if (parentLang === 'en') return 'en-US';
      if (parentLang === 'fr') return 'fr';
      if (parentLang === 'de') return 'de';
      if (parentLang === 'es') return 'es';
      if (parentLang === 'ja') return 'ja';
      if (parentLang === 'ko') return 'ko';
      return 'en-US'; // fallback
    }
    return localStorage.getItem('assetInventoryLanguage') || 'zh-CN';
  }

  // 设置语言
  function setLanguage(lang) {
    localStorage.setItem('assetInventoryLanguage', lang);
    console.log('🌐 语言已切换到:', lang);
  }

  // 翻译函数
  function t(key, params) {
    const lang = getCurrentLanguage();
    // 先找当前语言，找不到则 fallback 到 en-US，再找不到返回 key
    let text = translations[lang]?.[key] || translations['en-US']?.[key] || key;
    
    // 参数替换
    if (params) {
      Object.keys(params).forEach(paramKey => {
        text = text.replace(`{${paramKey}}`, params[paramKey]);
      });
    }
    
    return text;
  }

  // 导出到全局
  window.assetInventoryI18n = {
    t: t,
    getCurrentLanguage: getCurrentLanguage,
    setLanguage: setLanguage,
    translations: translations
  };

  console.log('✅ 资产盘点国际化模块已加载');
